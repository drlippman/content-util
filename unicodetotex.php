function unicodetotex($a) {
	global $unicode_to_tex;
	$offset = 0;
	$out = '';
	while ($offset >= 0) {
		$ord = ordutf8($a,$offset);
		if ($ord<128) {
			$out .= chr($ord);
		} else {
			$hex = strtoupper(dechex($ord));
			if (isset($unicode_to_tex[$hex])) {
				$out .= $unicode_to_tex[$hex].' ';
			} else {
				$out .= ' ';
				echo "unrecognized character, code $ord ($hex)<br/>";
			}
		}
	}
	return $out;
}

